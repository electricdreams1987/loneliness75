import { GameStats } from '@/types/game';

export function getScenario(endingId: string, stats: GameStats): string {
  const hasHighFreedom = stats.freedom >= 60;
  const hasHighCareer = stats.career >= 60;

  const intro = hasHighFreedom && hasHighCareer
    ? 'あなたは若い時代に自由とキャリアを非常に大切にしてきました。その選択は、20代から40代の生活に大きな充実と輝きをもたらし、多くの目標を達成させました。'
    : 'あなたは自分のペースを保ちながら、日々の課題や仕事に向き合って人生を歩んできました。';

  switch (endingId) {
    case 'relation_stable':
      return `${intro}
      
その過程で、あなたは周囲の人々への配慮と関係のメンテナンスを決して怠りませんでした。75歳になった現在、あなたの元には週末になると子どもや孫、あるいは長年の友人たちが自然と集まります。

日常のちょっとした困りごとがあっても、隣人や近くの知人が「どうしたの？」と声をかけてくれる環境が整っています。心身に不安はあっても、「自分を必要とし、気にかけてくれる人がいる」という確信が、あなたの老後を温かく守っています。`;

    case 'freedom_unstable':
      return `${intro}
      
一方で、長期的な関係づくりや、家族・次世代との確固たる絆、仕事以外の居場所は少し後回しになりました。

75歳になった今、朝起きてから夜眠るまで、何をするのも自由です。誰からも指図されない気楽さはあるものの、ふと「今週、宅配便の配達員以外と一言も喋っていない」と気づき、深い静寂に包まれる部屋で寂しさが胸に押し寄せることがあります。日常的にあなたの安否を気にかけてくれる人の存在は、非常に限られているのが現実です。`;

    case 'money_isolation':
      return `${intro}
      
ビジネスと資産形成に全力を注いだ結果、あなたの銀行口座には豊かな老後を送るのに十分すぎる資金が残されています。

しかし、75歳の今、あなたが暮らす立派な住まいは静まり返っています。お金で高品質な介護サービスや食事、身の回りの手配は完璧にできますが、そこに「損得勘定のない温かな笑顔」はありません。あなたが倒れた時に駆けつけるのは、契約に基づくサービススタッフのみ。お金はあるけれど、心を通わせる相手が誰もいないという、皮肉な現実がここにあります。`;

    case 'next_gen_heritage':
      return `${intro}
      
何より、あなたは自分のことだけでなく、「次の世代のために何ができるか」を常に考え、行動してきました。

75歳を迎えた現在、かつてあなたが育てた後輩、支援した若者、あるいはあなたの背中を見て育った子どもたちが、定期的にあなたの自宅を訪ねたり、メールで近況を報告してくれます。「あなたのあの一言が、今の私を支えています」という感謝の言葉が、あなたの胸に深く響きます。肉体は衰えても、あなたの知恵や想いは他者の中に生き続け、輝かしい意味資本となっています。`;

    case 'family_dependence':
      return `あなたはこれまで、何よりも家族を第一に考え、家庭内の絆を深く強く育んできました。75歳になった今も、パートナーや子どもたちとの団らんはあなたの最大の心の支えです。
      
しかし、その一方で、家族以外の「友人」「地域」「仕事以外のコミュニティ」との関わりをほぼ作ってきませんでした。

現在は問題ありませんが、もしパートナーと死別したり、子ども世帯が遠方に転勤・独立したりした場合、あなたのセーフティネットは一瞬にして崩壊する危険性を秘めています。「家族さえいれば大丈夫」という強い絆が、裏を返せば唯一の命綱であり、その糸が切れたときの孤立リスクは非常に高い状態です。`;

    case 'community_coexistence':
      return `${intro}
      
同時に、あなたは定年前後から「仕事以外の自分の居場所」を地域や趣味の場にコツコツと作ってきました。

75歳の今、あなたは街のちょっとした有名人かもしれません。自治会の寄り合い、趣味のサークル、あるいは行きつけの店に行けば、必ず誰かが「あ、こんにちは！」と声をかけてくれます。血縁や特別な肩書がなくても、地域社会の確かな一員として包摂されている実感が、あなたの日々に健康的な活力を与え、日常的な孤独感からあなたを遠ざけています。`;

    case 'career_isolation':
      return `あなたはかつて、仕事の第一線で大いなる成果を挙げ、組織や業界の中で輝かしいスポットライトを浴びてきました。そのプロフェッショナルとしての歩みは誇るべきものです。
      
しかし、定年を迎え、現役という「戦場」を退いたとき、仕事上の名刺や肩書で繋がっていた人々は驚くほど速やかにあなたの元から去っていきました。

75歳の今、手元には過去の表彰状や写真が残るのみです。プライベートでの人間関係や、地位に関係のない素の自分としてのコミュニティを作ってこなかったため、平日は終日予定がなく、かつての部下や同僚からの連絡も途絶えています。過去の栄光と現在の静寂のギャップに、寂しさを抱えながら暮らしています。`;

    case 'critical_isolation':
      return `気がつけば75歳。自由や目の前の仕事、あるいは自分一人の生活を守ることに終始してきた結果、あなたの周囲からはほとんどすべての人間関係の糸が途切れてしまっています。
      
家族との連絡はなく、近隣との挨拶もなく、何かあったときに気にかけてくれる友人のあてすらありません。

現在のあなたは、もし急な病気やトラブルに見舞われても、発見や支援が大幅に遅れる「孤立無援」の危機に極めて近い状態です。このシナリオは未来の確定ではありませんが、今この瞬間から、現実世界で誰かと小さな挨拶を交わすような、関係の結び直しを始める必要があります。`;

    default:
      return `${intro}
      
75歳の現在、大きなトラブルこそないものの、人間関係は少し内向きでこぢんまりとしたものになっています。

日常会話を交わす相手は数人に限られており、もしその人々に何かがあれば、あなたも一気に孤立してしまう可能性があります。これからの晩年をより豊かで安心できるものにするために、今のうちから少しずつ、周囲や新たなコミュニティへ視野を広げ、つながりの糸を増やしていくことをお勧めします。`;
  }
}
